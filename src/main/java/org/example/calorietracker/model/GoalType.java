package org.example.calorietracker.model;

/**
 * Перечисление, представляющее возможные цели пользователя в системе.
 * Определяет тип диеты и рекомендации по калорийности.
 *
 * <p>Примеры использования:
 * <pre>
 * user.setGoal(GoalType.WEIGHT_LOSS);
 * if (user.getGoal() == GoalType.MAINTENANCE) { ... }
 * </pre>
 */
public enum GoalType {
    /**
     * Цель - снижение веса.
     * Система будет рекомендовать дефицит калорий.
     */
    WEIGHT_LOSS,

    /**
     * Цель - поддержание текущего веса.
     * Система будет рекомендовать поддерживающую калорийность.
     */
    MAINTENANCE,

    /**
     * Цель - набор веса.
     * Система будет рекомендовать профицит калорий.
     */
    WEIGHT_GAIN
}
